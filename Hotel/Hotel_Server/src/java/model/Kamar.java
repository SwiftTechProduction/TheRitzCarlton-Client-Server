package model;

/**
 *
 * @author Lukas
 */
public class Kamar 
{
    private String kodeKamar;
    private String namaKamar;
    private String lantaiKamar;
    private String tipeKamar;
    private String harga;

    public String getKodeKamar() 
    {
        return kodeKamar;
    }

    public void setKodeKamar(String kodeKamar) 
    {
        this.kodeKamar = kodeKamar;
    }

    public String getNamaKamar() 
    {
        return namaKamar;
    }

    public void setNamaKamar(String namaKamar) 
    {
        this.namaKamar = namaKamar;
    }

    public String getLantaiKamar() 
    {
        return lantaiKamar;
    }

    public void setLantaiKamar(String lantaiKamar) 
    {
        this.lantaiKamar = lantaiKamar;
    }

    public String getTipeKamar() 
    {
        return tipeKamar;
    }

    public void setTipeKamar(String tipeKamar) 
    {
        this.tipeKamar = tipeKamar;
    }

    public String getHarga() 
    {
        return harga;
    }

    public void setHarga(String harga) 
    {
        this.harga = harga;
    }
    
}
